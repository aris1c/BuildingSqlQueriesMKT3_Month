--SALES3_MONTH
SELECT TO_CHAR(TGL,'YYYY') TAHUN, TO_CHAR(TGL,'MM') BULAN, OUTLET_ID,
    REFEREE_ID,
    DECODE(D.NAME,
        'APD-CS','00000',
        '-','00000',
        '000000','00000',A.DOCTOR_ID) DOCTOR_ID,
        SUM(J_PSN - R_PSN)PATIENT,
        SUM(J_BRUTO) BRUTO,
        SUM(J_DISKON) DISKON, 
        SUM(J_NETTO) NETTO, 
        SUM(R_NETTO) RETUR
FROM SALES3_ARIS A, DOCTOR_ARIS D
WHERE A.DOCTOR_ID = D.DOCTOR_ID(+)
    AND  
   A.TGL >= TO_DATE(TO_CHAR(SYSDATE-60,'YY-MM')||'-01','yy/MM/DD')--TO_DATE('1-'||TO_CHAR(SYSDATE-60,'MM-YY'))
   --A.TGL >= '1 JAN 21'
GROUP BY 
TO_CHAR(TGL,'YYYY') , TO_CHAR(TGL,'MM') , OUTLET_ID,
    REFEREE_ID,
    DECODE(D.NAME,
        'APD-CS','00000',
        '-','00000',
        '000000','00000',A.DOCTOR_ID)